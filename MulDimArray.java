public class MulDimArray {
    public static void main(String[] args) {
        int row =2;
        int col =2;
        int[] [] arrys= new int [row][col];
        for(int i=0;i<row;i++){
            for(int j=0;j<col;j++){
                System.out.println(arrys[i][j]+" ");
            }
        }
    }
}
